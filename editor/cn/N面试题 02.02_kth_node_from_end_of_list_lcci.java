/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    int res = 0;
    int count = 0;

    public int kthToLast(ListNode head, int k) {
        count = k;
        deep(head);
        return res;
    }

    private void deep(ListNode head){
        if(head.next != null ){
            deep(head.next);
        }

        if(count == 1){
            res = head.val;
        }

        count--;

    }
}
