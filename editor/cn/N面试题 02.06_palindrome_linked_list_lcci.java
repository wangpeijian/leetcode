/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode(int x) { val = x; }
 * }
 */
class Solution {
    ListNode p = null;
    boolean flag = true;

    public boolean isPalindrome(ListNode head) {
        p = head;
        revert(head);
        return flag;
    }

    private int revert(ListNode node){
        if(node == null){
            return 0;
        }

        if(node.next != null){
            int  value  = revert(node.next);

            if(p.val != value){
                flag = false;
            }

            p = p.next;
        }

        return node.val;
    }
}
